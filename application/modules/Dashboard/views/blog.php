    function sendEmailOtp(evt) {
        var email = evt.value //document.getElementById('confirm_email').value;
        console.log(email);
        if (email != '') {
            var formData = new FormData();
            var csrf = document.getElementsByName("csrf_test_name")[0].value;
            formData.append("csrf_test_name", csrf);
            formData.append("email", email);
            fetch("https://thericaverse.net/Dashboard/User/getMailOtp/", {
                    method: "POST",
                    headers: {
                        "X-Requested-With": "XMLHttpRequest"
                    },
                    body: formData,
                })
                .then(response => response.json())
                .then(result => {
                    document.getElementsByName("csrf_test_name")[0].value =
                        result.token;
                    console.log(result)
                    if (result.success == '1') {
                        toastr.success(result.message, {
                          timeOut: 5000
                        })
                    } else {
                        toastr.error(result.message, {
                          timeOut: 5000
                        })
                    };
                });
        } else {
          toastr.error('Email is required', {
                          timeOut: 5000
                        })
        }
    }


    function total_hub(evt) {
